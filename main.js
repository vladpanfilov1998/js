let content = document.createElement("div");
content.setAttribute('id', 'content');
let wrap = document.createElement("div");
wrap.setAttribute('id', 'wrap');
document.body.append(content, wrap);
let idRule = 0;
for (const item of rules) {
    let rule = document.createElement("div");
    rule.classList.add('rules', 'rule' + ++idRule);
    let ruleH2 = document.createElement('h2');
    ruleH2.innerText = item.title;
    let ruleP = document.createElement("p");
    ruleP.innerText = item.body;
    rule.append(ruleH2, ruleP);
    wrap.appendChild(rule)
}